
public class Winner {

    //attributes

    private String winner;



    //constructors

    public Winner() {

        winner ="";

    }


    public Winner (String win) {

        winner = win;
    }


    //setters and getters


    public void setWinner (String win) {

        winner = win;
        
    }

    public String getWinner() {



        return winner;
    }
    
}
